#include "ft_printf.h"

int	ft_string(char *str)
{
	if (!str)
	{
		write (1, "(null)", 6);
		return (6);
	}
	else
	{
		ft_putstr_fd(str, 1);
		return (ft_strlen(str));
	}
}
